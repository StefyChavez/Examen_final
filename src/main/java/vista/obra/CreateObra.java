     private void actualizarTabla2()
    {
        String [] encabezado = new String[4];
        encabezado[0]="C贸digo";
        encabezado[1]="Nombre";
        encabezado[2]="Obra";
        encabezado[3]="Nombre Autor";
        
        var datos = new Object[this.obraControl.listar().size()][4];
        
        var i=0;
        for(var obra:this.obraControl.listar())
        {
            datos[i][0]=obra.getCodigo();
            datos[i][1]=obra.getNombre();
            datos[i][2]=obra.getObra();
            datos[i][3]=obra.getAutor().getNombre();
            i++;
        }
        this.modeloTabla = new DefaultTableModel(datos,encabezado);
        this.jTable2.setModel(modeloTabla);
    }
